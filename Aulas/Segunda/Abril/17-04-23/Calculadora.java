public class Calculadora {
    public int soma(int a, int b){
        // usando variavel auxiliar
        int result = a + b;
        return result;
    }
    public int subtracao(int a, int b){
        // sem variavel auxiliar
        return a - b;
    }
    public int multiplicacao(int a, int b){
        return a * b;
    }
    public int divisao(int a, int b){
        return a / b;
    }
}

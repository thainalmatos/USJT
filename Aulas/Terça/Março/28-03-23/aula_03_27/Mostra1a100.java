//exibir os inteiros de 1 a 100: 1 2 3 4 5....100
public class Mostra1a100 {
    public static void main (String[] args) {
        System.out.println ("oi, sei contar de 1 a 100:");
        int cont = 1;
        while (cont <= 100) {
            System.out.print (cont + " ");
            cont = cont + 1;
        }
    }
}
